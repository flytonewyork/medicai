"use client";

import Link from "next/link";
import { useParams } from "next/navigation";
import { useLiveQuery } from "dexie-react-hooks";
import { db } from "~/lib/db/dexie";
import { PageHeader } from "~/components/ui/page-header";
import { Card, CardContent, CardHeader, CardTitle } from "~/components/ui/card";
import { Button } from "~/components/ui/button";
import { PillarTile } from "~/components/assessment/pillar-card";
import { useLocale } from "~/hooks/use-translate";
import { formatDate } from "~/lib/utils/date";
import { testById } from "~/lib/assessment/catalog";

export default function ViewAssessmentPage() {
  const locale = useLocale();
  const params = useParams<{ id: string }>();
  const id = Number(params?.id);
  const a = useLiveQuery(
    () => (Number.isFinite(id) ? db.comprehensive_assessments.get(id) : undefined),
    [id],
  );

  if (!a) {
    return <div className="p-6 text-sm text-ink-500">Loading…</div>;
  }

  const scores = [
    { label: locale === "zh" ? "功能" : "Function", v: a.functional_score },
    { label: locale === "zh" ? "症状" : "Symptoms", v: a.symptom_score },
    { label: locale === "zh" ? "毒性" : "Toxicity", v: a.toxicity_score },
    { label: locale === "zh" ? "心理" : "Mental", v: a.mental_score },
    { label: locale === "zh" ? "灵性" : "Spiritual", v: a.spiritual_score },
  ];

  return (
    <div className="mx-auto max-w-4xl space-y-6 p-4 md:p-8">
      <PageHeader
        title={`${locale === "zh" ? "评估" : "Assessment"} · ${formatDate(a.assessment_date, locale)}`}
        subtitle={a.trigger === "baseline" ? (locale === "zh" ? "基线" : "Baseline") : a.trigger}
        action={
          <Link href="/assessment">
            <Button variant="secondary">
              {locale === "zh" ? "返回" : "Back"}
            </Button>
          </Link>
        }
      />

      {(a.helper_role && a.helper_role !== "self") || a.helper_notes ? (
        <Card>
          <CardHeader>
            <CardTitle>
              {locale === "zh" ? "本次带做" : "Session helper"}
            </CardTitle>
          </CardHeader>
          <CardContent className="space-y-2 text-sm text-ink-700">
            {a.helper_role && a.helper_role !== "self" && (
              <div>
                <span className="eyebrow mr-2">
                  {locale === "zh" ? "带做人" : "Helper"}
                </span>
                {a.helper_name?.trim() || (locale === "zh" ? "（未填姓名）" : "(name not entered)")}
                <span className="ml-1 text-ink-400">
                  · {a.helper_role}
                </span>
              </div>
            )}
            {a.helper_notes && (
              <div>
                <div className="eyebrow">
                  {locale === "zh" ? "环境备注" : "Setup notes"}
                </div>
                <p className="mt-0.5 whitespace-pre-line">{a.helper_notes}</p>
              </div>
            )}
          </CardContent>
        </Card>
      ) : null}

      {typeof a.anchor_index === "number" && (
        <Card>
          <CardHeader>
            <CardTitle>Anchor Index</CardTitle>
            <div className="mt-1 text-sm text-ink-500">
              {locale === "zh"
                ? "加权合成分。功能权重最高。"
                : "Function-weighted composite across all five pillars."}
            </div>
          </CardHeader>
          <CardContent>
            <div className="serif text-5xl tabular-nums text-ink-900">
              {a.anchor_index}
              <span className="mono ml-2 text-base font-normal tracking-[0.04em] text-ink-400">
                / 100
              </span>
            </div>
            <div className="mt-4 grid gap-3 sm:grid-cols-2 md:grid-cols-3">
              {scores.map((s) =>
                typeof s.v === "number" ? (
                  <PillarTile key={s.label} label={s.label} score={s.v} />
                ) : null,
              )}
            </div>
          </CardContent>
        </Card>
      )}

      {(a.ai_summary_patient || a.ai_summary_clinician) && (
        <Card>
          <CardHeader>
            <CardTitle>{locale === "zh" ? "AI 概要" : "AI summary"}</CardTitle>
            <div className="mt-1 text-xs text-ink-500">
              {locale === "zh" ? "由 " : "Generated by "}
              {a.ai_summary_model ?? "Claude"}
            </div>
          </CardHeader>
          <CardContent className="space-y-4 text-sm">
            {a.ai_summary_patient && (
              <div>
                <div className="eyebrow">
                  {locale === "zh" ? "给患者" : "For the patient"}
                </div>
                <p className="mt-1 whitespace-pre-line">{a.ai_summary_patient}</p>
              </div>
            )}
            {a.ai_summary_clinician && (
              <div>
                <div className="eyebrow">
                  {locale === "zh" ? "给医师" : "For the clinician"}
                </div>
                <p className="mt-1 whitespace-pre-line">{a.ai_summary_clinician}</p>
              </div>
            )}
          </CardContent>
        </Card>
      )}

      <Card>
        <CardHeader>
          <CardTitle>
            {locale === "zh" ? "测试覆盖" : "Tests covered"}
          </CardTitle>
        </CardHeader>
        <CardContent>
          <ul className="space-y-1 text-sm">
            {(a.tests_included ?? []).map((id) => {
              const def = testById(id as never);
              if (!def) return null;
              const done = (a.tests_completed ?? []).includes(id);
              return (
                <li key={id} className="flex items-center justify-between">
                  <span>{def.title[locale]}</span>
                  <span
                    className={
                      done
                        ? "mono text-[10.5px] uppercase tracking-[0.1em] text-[var(--ok)]"
                        : "mono text-[10.5px] uppercase tracking-[0.1em] text-[var(--sand-2)]"
                    }
                  >
                    {done
                      ? locale === "zh"
                        ? "完成"
                        : "complete"
                      : locale === "zh"
                        ? "跳过"
                        : "skipped"}
                  </span>
                </li>
              );
            })}
          </ul>
        </CardContent>
      </Card>

      {a.values_statement && (
        <Card>
          <CardHeader>
            <CardTitle>{locale === "zh" ? "价值观" : "Values"}</CardTitle>
          </CardHeader>
          <CardContent>
            <p className="whitespace-pre-line text-sm">{a.values_statement}</p>
          </CardContent>
        </Card>
      )}
    </div>
  );
}
